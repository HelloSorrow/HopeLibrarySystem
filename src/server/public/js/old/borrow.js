/*用户借阅的函数*/
(function(global,$){
	$(".js-borrow-btn").click(function(){
		var borrowID=$(this).attr("data-borrowID");
        console.log(borrowID);
		$.ajax({
        	dataType:"json",
        	async:true,
        	data:{"borrowID":borrowID},
        	type:"POST",
            url:"/book/borrow",
        	success:function(response){
        	    hlayer.alert({
        	        text:response.message,
                    time:2000,
                    mainBg:'#1c95ea',
                    icon:1
                });
                setTimeout(function(){
                    location.assign(location.href);
                },2000);
            },
        	error:function(){
                hlayer.alert({
                    text:'请求失败',
                    time:2000,
                    mainBg:'#1c95ea',
                    icon:2
                });
        	}
        })
	})
})(window,jQuery);